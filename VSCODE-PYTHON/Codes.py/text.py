'''
Analisador de Textos

name = str(input('Digite seu nome completo: ')).strip()
print('Analisando seu nome...')
print('Seu nome em maiúsculas é: {}'.format(name.upper()))
print('Seu nome em minúsculas é: {}'.format(name.lower()))
print('Seu nome tem ao todo {} letras'.format(len(name) - name.count(' ')))
print('Seu primeiro nome tem {} letras '.format((name.find(' '))))
'''

'''
Separando dígitos de um número

num = int(input('Informe um número: '))

u = num // 1 % 10
d = num // 10 % 10
c = num // 100 % 10
m = num // 1000 % 10

print('Analisando o número {}'.format(num))
print('Unidade: {}'.format(u))
print('Dezena: {}'.format(d))
print('Centena: {}'.format(c))
print('Milhar: {}'.format(m))
'''

'''

Verificando as primeiras letras de um texto

city = str(input('Informe a cidade você nasceu: ')).strip()

print(city[:5].upper() == 'SANTO')
'''

'''

Procurando uma string dentro de outra

name = str(input('Informe seu nome completo: ')).strip()

print('Seu nome tem Silva: {}'.format('Silva' in name.lower()))
'''

'''
Primeira e última ocorrência de uma string

frase = str(input('Digite uma frase: ')).upper().strip()

print('A letra A aparece {} vezes na frase'.format(frase.count('A')))
print('A primeira letra A apareceu na posição {}'.format(frase.find('A')+1))
print('A primeira letra A apareceu na posição {}'.format(frase.rfind('A')+1))
'''


'''
Primeiro e último nome de uma pessoa

name = str(input('Informe seu nome completo: ')).strip()
nome = name.split()
print('Muito prazer em te conhecer')
print('Seu primeiro nome é {}'.format(nome[0]))
print('Seu ultimo nome é {}'.format(nome[len(nome)-1]))
'''
